package ej;

public class CalcRect {

    private int width;
    private int length;
    private int height;


    // WIDTH
    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    // HEIGHT
    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    // LENGTH
    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }



}
